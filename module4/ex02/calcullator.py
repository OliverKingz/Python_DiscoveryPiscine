#!/usr/bin/env python3
nbr1 = int(input("Give me the first number: "))
nbr2 = int(input("Give me the second number: "))
print("Thank you!")
print(nbr1, "+", nbr2, "=", nbr1 + nbr2)
print(nbr1, "-", nbr2, "=", nbr1 - nbr2)
print(nbr1, "/", nbr2, "=", int(nbr1 / nbr2))
print(nbr1, "*", nbr2, "=", nbr1 * nbr2)